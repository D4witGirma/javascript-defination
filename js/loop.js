//Loops can execute a block of code a number of times.
//there are three types of loop for,while and do while


//printing number from one to 10 using do while,for and while


//for loop
for(let a=0; a<10; a++)
{
    console.log(a);
}
//while loop

let a=0; 

while(a<10)
{
    console.log(a);
    a++
}
//do while loop
let b=0;
do{
    console.log(b);
    b++;

}while(a<10);